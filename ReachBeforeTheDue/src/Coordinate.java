
public class Coordinate {
	int x = -1;
	int y = -1;
	public Coordinate(int x, int y){
		this.x = x;
		this.y = y;
	}
	
	public void setX(int x){
		this.x = x;
	}
	
	public void setY(int y){
		this.y = y;
	}
	
	public int getX(){
		return this.x;
	}
	
	public int getY(){
		return this.y;
	}
	
	public Coordinate getCoordinate(){
		return this;
	}
}
