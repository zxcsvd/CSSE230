package hardysTaxi;

/**
 * An exception type to be thrown if the parameters passed
 * to a TaxicabNumber constructor are not valid.
 * 
 * @author anderson  November, 2010.
 *
 */
@SuppressWarnings("serial")
public class HardyException extends RuntimeException {

	/**
	 * Creates a new Hardy Exception.
	 */
	public HardyException () { /* empty */ }
	
	
	/**
	 * Creates a new Hardy Exception with the given message.
	 *
	 * @param message
	 */
	public HardyException(String message) {
		super(message);
	}
}